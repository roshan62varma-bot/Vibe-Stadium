export function getTranslation(lang: Language, keyPath: string, params?: Record<string, string | number>): string {
  const parts = keyPath.split(".");
  let current: any = translations[lang] || translations.en;
  
  for (const part of parts) {
    if (current && typeof current === "object" && part in current) {
      current = current[part];
    } else {
      // Fallback to English
      let fallbackCurrent: any = translations.en;
      for (const fPart of parts) {
        if (fallbackCurrent && typeof fallbackCurrent === "object" && fPart in fallbackCurrent) {
          fallbackCurrent = fallbackCurrent[fPart];
        } else {
          return keyPath;
        }
      }
      current = fallbackCurrent;
      break;
    }
  }

  if (typeof current !== "string") {
    return keyPath;
  }

  let text = current;
  if (params) {
    Object.entries(params).forEach(([key, val]) => {
      text = text.replace(`{${key}}`, String(val));
    });
  }

  return text;
}

export function useTranslation() {
  // Read language state from Zustand store if needed, or fallback
  const store = useStore() as any;
  const lang: Language = store?.language || "en";
  
  return {
    t: (keyPath: string, params?: Record<string, string | number>) => getTranslation(lang, keyPath, params),
    language: lang,
    isRtl: lang === "ar"
  };
}
